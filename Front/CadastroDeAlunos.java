    private void txtemailFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtemailFocusLost
        String email =txtemail.getText();
        if(!email.contains("@")){
            JOptionPane.showMessageDialog(null,"Email inválido!","Validação",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_txtemailFocusLost
    public boolean verificarTurma(){
        try { 
            double turma=Double.parseDouble(txtclasse.getText());
            return true;
        }catch (NumberFormatException erro){ 
            JOptionPane.showMessageDialog(null, "O campo turma só pode conter números!","Validação",JOptionPane.ERROR_MESSAGE);
            return false;
        }
    }
